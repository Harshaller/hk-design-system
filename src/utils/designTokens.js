// Helper function to generate CSS variables
export const generateCSSVariables = () => {
  const cssVars = {};
  
  // Colors
  Object.entries(colorTokens).forEach(([category, values]) => {
    if (typeof values === 'object') {
      Object.entries(values).forEach(([key, value]) => {
        if (typeof value === 'string') {
          cssVars[`--color-${category}-${key}`] = value;
        } else if (typeof value === 'object') {
          Object.entries(value).forEach(([shade, color]) => {
            cssVars[`--color-${category}-${key}-${shade}`] = color;
          });
        }
      });
    }
  });
  
  // Spacing
  Object.entries(spacingTokens).forEach(([key, value]) => {
    cssVars[`--spacing-${key}`] = value;
  });
  
  // Typography
  Object.entries(typographyTokens).forEach(([category, values]) => {
    Object.entries(values).forEach(([key, value]) => {
      cssVars[`--font-${category}-${key}`] = value;
    });
  });
  
  return cssVars;
};